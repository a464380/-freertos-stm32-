// 定义包含动态数组的结构体
struct DynamicArray {
    uint8_t size;
    uint8_t *array;
};
 
// 初始化结构体及动态数组
void initDynamicArray(struct DynamicArray *dynArray, uint8_t size) {
    dynArray->size = size;
    dynArray->array = (uint8_t *)malloc(size * sizeof(uint8_t));
	return;
}
 
// 释放动态分配的内存
void freeDynamicArray(struct DynamicArray *dynArray) {
    free(dynArray->array);
    dynArray->array = NULL;
    dynArray->size = 0;
}
struct DynamicArray myArray;
uint8_t RecvTmpBuf[50] = {0};
//uart接收idle中断回调函数
void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart, uint16_t Size)
{
	if(huart == & huart2)
	{
		//HAL_UART_Transmit_DMA(&huart2, RecvTmpBuf, Size);
		//动态分配数组
		initDynamicArray(&myArray, Size);
		uint8_t i = 0;
		
		
		for(i = 0; i < Size; i++)
		{
			myArray.array[i] = RecvTmpBuf[i];
		}
		

		if(strcmp(myArray.array, "unlock") == 0)					//解锁信号
		{
			if(lockState == 1)
			{
				//解锁语音
				broadcast_sound(4, 1000);
				lockState = 0;
				HAL_UART_Transmit_DMA(&huart2, "unlock", 6);
			}
		}
		else if(strcmp(myArray.array, "locked") == 0)		//上锁信号
		{
			if(lockState == 0)
			{
				broadcast_sound(0, 1000);
				lockState = 1;
				HAL_UART_Transmit_DMA(&huart2, "lock", 4);
			}
		}
		else if(strcmp(myArray.array, "add") == 0)			//添加指纹
		{
			addfingerstate = 1;
			HAL_UART_Transmit_DMA(&huart2, "add", 4);
		}
		else if(strcmp(myArray.array, "cle") == 0)		//清空指纹库
		{
			removefingerstate = 1;
			//清空指纹库语音
			broadcast_sound(5, 1000);
			HAL_UART_Transmit_DMA(&huart2, "clear", 5);
		}
		else if(strcmp(myArray.array, "one") == 0)			//设置5音量
		{
			set_volume(5, 1000);
		}
		else if(strcmp(myArray.array, "two") == 0)			//设置10音量
		{
			set_volume(0x0A, 1000);
		}
		else if(strcmp(myArray.array, "thr") == 0)			//设置15音量
		{
			set_volume(0x0F, 1000);
		}
		else if(strcmp(myArray.array, "fou") == 0)			//设置20音量
		{
			set_volume(0x14, 1000);
		}
		else if(strcmp(myArray.array, "fiv") == 0)			//设置25音量
		{
			set_volume(0x19, 1000);
		}
		else if(strcmp(myArray.array, "six") == 0)			//设置30音量
		{
			set_volume(0x1E, 1000);
		}
		else
		{
			HAL_UART_Transmit_DMA(&huart2, "error", 5);
		}
		
		//释放数组
		freeDynamicArray(&myArray);
		HAL_UARTEx_ReceiveToIdle_DMA(&huart2, RecvTmpBuf, 50);
	}
}
